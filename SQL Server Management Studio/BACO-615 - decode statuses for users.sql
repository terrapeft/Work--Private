select uEmailAddress as Email, pname as Site, pid as Publication, /*sstatus as Status,*/ string_agg(s.stName, ', ') as Permissions
from statuses s
join publications p on s.stpid = p.pid
join subscriptions su on su.spid = p.pid
join userdetails u on su.suid = u.uid
where pid in (235, 234, 5014, 5018, 5021, 5020, 5019, 223, 5047, 5013, 5012, 5011, 225, 5045, 404, 267, 5010, 5044)
and sstatus & s.stMask = s.stMask
and uUsername in (
	select email from @users
)
group by uEmailAddress, pname, pid, sstatus
order by uEmailAddress, pname
